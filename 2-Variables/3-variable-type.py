# Show the variable data type by using type() function 
x = 1 
print(type(x)) # int type
x = 1.0 
print(type(x)) #float type
x = '1'
print(type(x)) #string type
x = True
print(type(x)) #bolean type
x = None
print(type(x)) #None type

#Convert variable data type
x = str(5)    # x will be '5'
y = int('5')    # y will be 5
